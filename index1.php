           <p>Swahili (also Kiswahili) is an agglutinative language widely spoken in East Africa. It originated between 1500 and 1600 as a auxiliary language for the traders of the east African coast and the Middle East, particularly Oman. Swahili is the mother tongue for many inhabitants of the central east African coast (particularly in Zanzibar, Mombasa, Pemba and Lamu), and it is a lingua franca for up to 50 million others.</p>
						<p>The traditional centre of the language is Zanzibar, and Swahili is an official language of Tanzania, Kenya and Uganda. The Swahili spoken in Nairobi incorporates significantly more English loanwords than that spoken on the coast, and in Tanzania Swahili is the most widely used language. The language is also spoken in regions that border these three countries, such as far northern Malawi and Mozambique, eastern Democratic Republic of the Congo and Rwanda, and southern Ethiopia. The Zanzibar dialect is known as Kiunguja.</p>
